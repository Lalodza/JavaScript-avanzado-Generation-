/* 
    OPERADORES REALCIONALES
    < > , >= <= , == === , != !==
    boolean true false
*/

console.log(7 < 7);
console.log(7 <= 7);

console.log(7 == '7');
console.log(7 === '7');

console.log(7 != '7');
console.log(7 !== '7');

/* 
    INCREMENTO Y DECREMENTO
    +=  -=  *=  /=
*/

let incremento = 5;
incremento += 5;
console.log(incremento);
incremento -= 5;
console.log(incremento);
incremento *= 5;
console.log(incremento);
incremento /= 5;
console.log(incremento);

/* 
    OPERADOR UNARIO
    ++ , --
*/

let sumar = 0;
console.log(sumar + ' linea 1era');
sumar ++;
console.log(sumar + ' linea 2da');
sumar ++;
console.log(sumar);
sumar --;
sumar --;
console.log(sumar);


